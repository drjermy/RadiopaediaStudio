// Codegen for src/main/radiopaedia-config.ts.
//
// Behaviour (see GitHub issue #13):
//   - If `RADIOPAEDIA_CLIENT_ID` / `RADIOPAEDIA_CLIENT_SECRET` are set in
//     the environment, write a fresh `radiopaedia-config.ts` with those
//     literals. This is the CI / packaging path.
//   - If neither is set AND `radiopaedia-config.ts` already exists, do
//     nothing so a dev can hand-maintain their local copy.
//   - If neither is set AND the file doesn't exist, seed it from
//     `radiopaedia-config.example.ts` so tsc has something to typecheck.
//
// Redirect URI defaults to `urn:ietf:wg:oauth:2.0:oob` (out-of-band):
// Radiopaedia's OAuth app-registration form rejects non-HTTPS redirect
// URIs, so a `http://127.0.0.1:<port>/callback` loopback isn't
// acceptable. OOB is the only viable redirect for a desktop app —
// the user authorises in the browser, Radiopaedia shows the code on its
// own confirmation page, and the user pastes it into our Settings panel.
// Override via `RADIOPAEDIA_REDIRECT_URI` only if you've specifically
// registered a different value.
//
// Never print the client_secret. Summary output must be redacted.

import { copyFileSync, existsSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const mainDir = join(root, 'src', 'main');
const targetFile = join(mainDir, 'radiopaedia-config.ts');
const exampleFile = join(mainDir, 'radiopaedia-config.example.ts');

const DEFAULT_REDIRECT_URI = 'urn:ietf:wg:oauth:2.0:oob';
const DEFAULT_API_BASE = 'https://radiopaedia.org';

const clientId = process.env.RADIOPAEDIA_CLIENT_ID ?? '';
const clientSecret = process.env.RADIOPAEDIA_CLIENT_SECRET ?? '';
const redirectUri = process.env.RADIOPAEDIA_REDIRECT_URI ?? DEFAULT_REDIRECT_URI;
const apiBase = (process.env.RADIOPAEDIA_API_BASE ?? DEFAULT_API_BASE).replace(/\/$/, '');

const haveEnvCreds = clientId !== '' || clientSecret !== '';
const haveEnvBase = process.env.RADIOPAEDIA_API_BASE != null && process.env.RADIOPAEDIA_API_BASE !== '';
const fileExists = existsSync(targetFile);

function tsLiteral(s) {
  // Escape backslashes, backticks, and `${` for a template-literal-free
  // single-quoted string literal. No quotes in client_id/secret from
  // Radiopaedia's console, but be defensive.
  return "'" + String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'") + "'";
}

function writeGenerated() {
  const body =
    `// AUTO-GENERATED by scripts/write-radiopaedia-config.mjs. Do not edit by hand.\n` +
    `// Gitignored — see src/main/radiopaedia-config.example.ts for the template.\n` +
    `\n` +
    `export const RADIOPAEDIA_CLIENT_ID = ${tsLiteral(clientId)};\n` +
    `export const RADIOPAEDIA_CLIENT_SECRET = ${tsLiteral(clientSecret)};\n` +
    `export const RADIOPAEDIA_REDIRECT_URI = ${tsLiteral(redirectUri)};\n` +
    `export const RADIOPAEDIA_API_BASE = ${tsLiteral(apiBase)};\n`;
  writeFileSync(targetFile, body, { mode: 0o600 });
}

if (haveEnvCreds || haveEnvBase) {
  writeGenerated();
  // Log presence only — never the secret itself. api_base is not a secret.
  const idSummary = clientId ? `set (${clientId.length} chars)` : 'unset';
  const secretSummary = clientSecret ? 'set' : 'unset';
  console.log(
    `[write-radiopaedia-config] wrote radiopaedia-config.ts ` +
      `(client_id ${idSummary}, client_secret ${secretSummary}, ` +
      `redirect_uri ${redirectUri}, api_base ${apiBase})`,
  );
} else if (!fileExists) {
  if (!existsSync(exampleFile)) {
    console.error(
      '[write-radiopaedia-config] neither env vars nor ' +
        'radiopaedia-config.example.ts are available; cannot seed ' +
        'radiopaedia-config.ts',
    );
    process.exit(1);
  }
  copyFileSync(exampleFile, targetFile);
  console.log(
    '[write-radiopaedia-config] seeded radiopaedia-config.ts from example ' +
      '(empty credentials; set RADIOPAEDIA_CLIENT_ID / _SECRET env vars or ' +
      'edit the file to configure OAuth)',
  );
} else {
  // File exists and no env overrides — leave the dev's hand-maintained copy alone.
  console.log(
    '[write-radiopaedia-config] radiopaedia-config.ts already present, ' +
      'no env overrides — leaving it as-is',
  );
}
